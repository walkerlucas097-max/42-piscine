#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	str[5];

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			str[0] = (a / 10 + '0');
			str[1] = (a % 10 + '0');
			str[2] = (' ');
			str[3] = (b / 10 + '0');
			str[4] = (b % 10 + '0');
			write(1, str, 5);
			if (!(a == 98 && b == 99))
				write(1, " ", 2);
			b++;
		}
		a++;
	}
}

